package com.java.inheritence;

import java.util.Scanner;

public class 措傈 extends 瘤档{
	private String[] 备;
	
	public void 备八祸(String 备) {
	
		super.林家 += 备;
	}

	@Override
	public String 林家() {
		
		Scanner scan = new Scanner(System.in);	
		String input_备 ="备";
		
		System.out.print("备甫 涝仿窍技夸 : ");
		input_备= scan.nextLine()+" ";
		
		备八祸(input_备);
		
		return super.林家();
	}

	public String[] get备() {
		return 备;
	}
	
}
